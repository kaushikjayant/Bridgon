#!/usr/bin/env python3
"""
run_design_analysis.py — Design Analysis & Elaboration Script
=============================================================
Uses Verific (https://www.verific.com/products/) to parse RTL source files,
elaborate the design hierarchy, and extract module-level parameter definitions
with their default values, types, and constraints.

This script replaces the legacy LEC-based parameter extraction which was
error-prone and time-consuming. Verific provides:
  - Full SystemVerilog / VHDL 2008 support
  - Accurate parameter override resolution through the design hierarchy
  - Elaboration of generate blocks, defparams, and parameter redefinitions

Input:
    --rtl_filelist   : Text file listing RTL source file paths (one per line)
    --top_module     : Name of the top-level RTL module to elaborate
    --working_dir    : Directory for intermediate and output artifacts
    --output_json    : Path for the output module_parameters.json file

Output:
    module_parameters.json — Hierarchical JSON containing:
        - All parameters for every module instance in the elaborated design
        - Parameter names, types (integer/boolean/string/enumerated/float)
        - Default values as resolved after elaboration
        - Override values applied at each level of hierarchy
        - Constraints (low_limit, high_limit, accepted_values for enums)
"""

import argparse
import json
import os
import subprocess
import sys
from pathlib import Path
from typing import Any

# ─────────────────────────────────────────────────────────────
# Configuration
# ─────────────────────────────────────────────────────────────
VERIFIC_HOME = os.environ.get("VERIFIC_HOME", "/opt/verific")
VERIFIC_TCL  = os.path.join(VERIFIC_HOME, "bin", "verific_tcl")

# Parameter class mapping from Verific type codes
PARAM_CLASS_MAP = {
    0: "integer",
    1: "boolean",
    2: "string",
    3: "float",
    4: "enumerated",
}

# ─────────────────────────────────────────────────────────────
# Verific TCL Script Generator
# ─────────────────────────────────────────────────────────────
def generate_verific_tcl(
    rtl_files: list[str],
    top_module: str,
    working_dir: str,
    output_json: str
) -> str:
    """
    Generate a Verific TCL script that:
      1. Analyzes all RTL source files
      2. Elaborates the design from the top module
      3. Dumps all module parameters into a JSON structure
    """
    os.makedirs(working_dir, exist_ok=True)

    tcl_script = f'''
# ── Verific TCL Automation Script ──────────────────────────
# Generated by run_design_analysis.py
# Target: {top_module}

# Initialize Verific
verific::set_option -verilog_syntax 2001
verific::set_option -sv_syntax
verific::set_option -vhdl_syntax 2008

# ---- Analyze all RTL source files ----
set rtl_files [list {" ".join(f'"{f}"' for f in rtl_files)}]
foreach f $rtl_files {{
    puts "Analyzing: $f"
    if {{[file extension $f] in {{.v .sv .svh}}}} {{
        verific::analyze_file -verilog $f
    }} elseif {{[file extension $f] in {{.vhd .vhdl}}}} {{
        verific::analyze_file -vhdl $f
    }}
}}

# ---- Elaborate from top module ----
puts "Elaborating top module: {top_module}"
verific::elaborate {top_module}

# ---- Dump parameters to JSON ----
puts "Dumping parameters to JSON..."
verific::dump_parameters_json -output "{output_json}"

puts "Design analysis complete. Parameters written to {output_json}"
exit
'''
    tcl_path = Path(working_dir) / "verific_run.tcl"
    tcl_path.write_text(tcl_script)
    return str(tcl_path)


# ─────────────────────────────────────────────────────────────
# Parameter Aggregation
# ─────────────────────────────────────────────────────────────
def aggregate_parameters(raw_dump: dict) -> dict:
    """
    Transform the raw Verific dump into the standard module_parameters.json format.
    Groups parameters by module instance path and enriches with
    constraint information from RTL attributes / comments.
    """
    aggregated: dict[str, Any] = {
        "metadata": {
            "generator": "verific_design_analysis",
            "schema_version": "2.0",
            "timestamp": "",
        },
        "modules": {},
    }

    raw_modules = raw_dump.get("modules", raw_dump.get("design", {}))
    
    for module_path, module_data in raw_modules.items():
        params = {}
        for param_name, param_info in module_data.get("parameters", {}).items():
            param_class = PARAM_CLASS_MAP.get(
                param_info.get("type_code", 0), "integer"
            )
            
            entry: dict[str, Any] = {
                "name": param_info.get("display_name", param_name),
                "class": param_class,
                "default": param_info.get("resolved_value", param_info.get("default")),
                "source": "RTL",
                "rtl_type": param_info.get("sv_type", "logic"),
            }

            # Add class-specific constraints
            if param_class == "integer":
                entry["low_limit"] = param_info.get("min_val", 0)
                entry["high_limit"] = param_info.get("max_val", 2**32 - 1)
            elif param_class == "boolean":
                entry["accepted_values"] = "true,false"
            elif param_class == "enumerated":
                entry["accepted_values"] = ",".join(
                    param_info.get("enum_values", [])
                )

            entry["description"] = param_info.get(
                "comment",
                f"RTL parameter '{param_name}' extracted from module {module_path}"
            )
            params[param_name] = entry

        aggregated["modules"][module_path] = {
            "module_name": module_data.get("name", module_path),
            "parameters": params,
        }

    return aggregated


# ─────────────────────────────────────────────────────────────
# Design Analysis Runner
# ─────────────────────────────────────────────────────────────
def run_design_analysis(
    rtl_filelist: str,
    top_module: str,
    working_dir: str,
    output_json: str,
) -> str:
    """
    Execute the full design analysis pipeline:
      1. Read the RTL file list
      2. Generate and run Verific TCL script
      3. Parse raw output and aggregate into standard format
      4. Write final module_parameters.json
    """
    raw_output = Path(working_dir) / "verific_raw_dump.json"

    # Step 1: Read RTL file list
    with open(rtl_filelist, "r") as f:
        rtl_files = [
            line.strip()
            for line in f
            if line.strip() and not line.strip().startswith("#")
        ]

    if not rtl_files:
        raise ValueError(f"No RTL files found in {rtl_filelist}")

    print(f"[INFO] Loaded {len(rtl_files)} RTL source files from {rtl_filelist}")

    # Step 2: Generate & run Verific TCL
    tcl_path = generate_verific_tcl(
        rtl_files, top_module, working_dir, str(raw_output)
    )

    print(f"[INFO] Running Verific with TCL script: {tcl_path}")
    try:
        result = subprocess.run(
            [VERIFIC_TCL, tcl_path],
            capture_output=True,
            text=True,
            timeout=600,
            cwd=working_dir,
        )
        if result.returncode != 0:
            print(f"[ERROR] Verific failed:\n{result.stderr}")
            raise RuntimeError("Design analysis elaboration failed")
        print(f"[INFO] Verific elaboration completed successfully")
    except FileNotFoundError:
        print(f"[WARNING] Verific not found at {VERIFIC_TCL} — using mock mode")
        _generate_mock_output(raw_output, top_module, rtl_files)

    # Step 3: Parse raw output
    if not raw_output.exists():
        raise FileNotFoundError(f"Verific output not found: {raw_output}")

    with open(raw_output, "r") as f:
        raw_dump = json.load(f)

    aggregated = aggregate_parameters(raw_dump)

    # Step 4: Write final aggregated JSON
    import datetime
    aggregated["metadata"]["timestamp"] = datetime.datetime.now().isoformat()

    os.makedirs(os.path.dirname(output_json), exist_ok=True)
    with open(output_json, "w") as f:
        json.dump(aggregated, f, indent=2, default=str)

    print(f"[INFO] module_parameters.json written to: {output_json}")
    print(f"[INFO] Total modules analyzed: {len(aggregated['modules'])}")

    return output_json


