# ─────────────────────────────────────────────────────────────
# CLI Entry Point
# ─────────────────────────────────────────────────────────────
def main():
    parser = argparse.ArgumentParser(
        description="Verific-based Design Analysis & Parameter Extraction"
    )
    parser.add_argument("--rtl_filelist", required=True,
                        help="Text file listing RTL source paths")
    parser.add_argument("--top_module", required=True,
                        help="Name of top-level RTL module")
    parser.add_argument("--working_dir", required=True,
                        help="Working directory for artifacts")
    parser.add_argument("--output_json", required=True,
                        help="Path for output module_parameters.json")

    args = parser.parse_args()

    try:
        output = run_design_analysis(
            rtl_filelist=args.rtl_filelist,
            top_module=args.top_module,
            working_dir=args.working_dir,
            output_json=args.output_json,
        )
        print(f"[SUCCESS] Design analysis complete → {output}")
    except Exception as e:
        print(f"[FATAL] {e}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    main()